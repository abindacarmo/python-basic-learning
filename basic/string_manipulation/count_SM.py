# count ne'e utiliza hodi sura dala hira mak buat ida mosu iha string nia laran

# exemplo
teks = "Abinda"
print(teks.count("a")) # output: 1, tamba letra a kiik ida deit


# case sensitive
teks = "Abinda"
print(teks.count("A"))


# bele mos sura liafuan
liafuna = "hau han, hau hemu, hau toba"
print(liafuna.count("hau"))

# bele mos sura liu letra ida
teks = "banana"
print(teks.count("an"))

# nia formatu mak hanesan
# string.count("yang mau dihitung") maibe string ne bele troka tau fali variabel mos diak maibe strign tenke rai iha variable nia laran

